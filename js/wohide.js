function woday1() {
  var x = document.getElementById("woday1");
  if (x.style.display == "none") {
    x.style.display = "block";
    x.style.webkitTransitionDuration = "1s";
    x.style.mozTransitionDuration = "1s";
      x.style.oTransitionDuration = "1s";
      x.style.transitionDuration = "1s";
      x.style.opacity = "0.5";
  } else {
    x.style.display = "none";
    x.style.webkitTransitionDuration = "1s";
    x.style.mozTransitionDuration = "1s";
      x.style.oTransitionDuration = "1s";
      x.style.transitionDuration = "1s";
      x.style.opacity = "0.5";
  }
}
